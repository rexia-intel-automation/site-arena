    <section class="page-header">
        <h1 class="page-title">Código de Conduta e Ética</h1>
        <p class="page-subtitle">Nossos valores e princípios para um ambiente íntegro e respeitoso</p>
    </section>

    <div class="legal-content">
        <div class="legal-section">
            <h2>1. Mensagem da Administração</h2>
            <div class="highlight-box">
                <p>A Arena BRB Mané Garrincha tem o compromisso de conduzir seus negócios com integridade, ética e transparência. Este Código de Conduta estabelece os princípios e valores que guiam nossas ações e decisões, aplicando-se a todos os colaboradores, parceiros, fornecedores e prestadores de serviço.</p>
                <p><strong>Nosso objetivo é construir um ambiente de trabalho saudável, inclusivo e baseado no respeito mútuo, sempre em conformidade com as leis e regulamentos aplicáveis.</strong></p>
            </div>
        </div>

        <div class="legal-section">
            <h2>2. Abrangência e Aplicabilidade</h2>
            <p>Este Código de Conduta aplica-se a:</p>
            <ul>
                <li><strong>Colaboradores:</strong> Todos os empregados, independentemente do cargo ou função;</li>
                <li><strong>Dirigentes:</strong> Diretores, gerentes e membros da administração;</li>
                <li><strong>Estagiários e Aprendizes:</strong> Todos os profissionais em formação;</li>
                <li><strong>Prestadores de Serviço:</strong> Terceirizados que atuam em nossas instalações;</li>
                <li><strong>Parceiros Comerciais:</strong> Fornecedores, consultores e empresas contratadas;</li>
                <li><strong>Representantes:</strong> Qualquer pessoa que atue em nome da Arena BRB.</li>
            </ul>
            <p>O descumprimento das diretrizes aqui estabelecidas pode resultar em medidas disciplinares, incluindo advertência, suspensão ou desligamento, além de eventuais ações legais cabíveis.</p>
        </div>

        <div class="legal-section">
            <h2>3. Nossos Valores Fundamentais</h2>

            <h3>3.1. Integridade</h3>
            <p>Agimos com honestidade, transparência e ética em todas as nossas relações, mantendo coerência entre o que dizemos e fazemos.</p>

            <h3>3.2. Excelência</h3>
            <p>Buscamos a melhoria contínua em nossos processos, serviços e resultados, oferecendo experiências memoráveis aos nossos públicos.</p>

            <h3>3.3. Respeito</h3>
            <p>Valorizamos a diversidade e promovemos um ambiente inclusivo, tratando todas as pessoas com dignidade e consideração.</p>

            <h3>3.4. Responsabilidade</h3>
            <p>Assumimos nossas obrigações com seriedade, respondendo por nossas ações e decisões perante colaboradores, clientes e sociedade.</p>

            <h3>3.5. Inovação</h3>
            <p>Estimulamos a criatividade e a busca por soluções inovadoras que agreguem valor aos nossos stakeholders.</p>

            <h3>3.6. Sustentabilidade</h3>
            <p>Comprometemo-nos com práticas ambientalmente responsáveis e socialmente justas para as gerações presentes e futuras.</p>
        </div>

        <div class="legal-section">
            <h2>4. Relacionamento no Ambiente de Trabalho</h2>

            <h3>4.1. Respeito e Dignidade</h3>
            <p>É dever de todos manter relações profissionais pautadas pelo respeito mútuo. São inaceitáveis:</p>
            <ul>
                <li>Assédio moral: intimidação, humilhação, constrangimento ou isolamento;</li>
                <li>Assédio sexual: condutas de conotação sexual não desejadas;</li>
                <li>Discriminação: por raça, cor, gênero, orientação sexual, religião, idade, deficiência, origem ou qualquer outra característica;</li>
                <li>Violência física ou psicológica;</li>
                <li>Linguagem ofensiva ou desrespeitosa.</li>
            </ul>

            <h3>4.2. Diversidade e Inclusão</h3>
            <p>A Arena BRB valoriza a diversidade em todas as suas formas e promove a equidade de oportunidades. Estamos comprometidos com:</p>
            <ul>
                <li>Processos seletivos justos e imparciais;</li>
                <li>Igualdade salarial para funções equivalentes;</li>
                <li>Acessibilidade e adaptações razoáveis para pessoas com deficiência;</li>
                <li>Combate a preconceitos e estereótipos;</li>
                <li>Criação de um ambiente seguro para todos.</li>
            </ul>

            <h3>4.3. Saúde e Segurança</h3>
            <p>Zelamos pela saúde e segurança de todos. É responsabilidade de cada um:</p>
            <ul>
                <li>Seguir normas de segurança e utilizar EPIs quando necessário;</li>
                <li>Relatar situações de risco ou acidentes;</li>
                <li>Manter o ambiente de trabalho limpo e organizado;</li>
                <li>Não trabalhar sob efeito de álcool ou substâncias ilícitas;</li>
                <li>Participar de treinamentos de segurança obrigatórios.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>5. Conflito de Interesses</h2>
            <p>Conflito de interesses ocorre quando interesses pessoais interferem ou podem interferir nas decisões profissionais. Devem ser evitadas situações como:</p>
            <ul>
                <li>Contratar ou supervisionar familiares diretos;</li>
                <li>Ter interesse financeiro em fornecedores ou concorrentes;</li>
                <li>Aceitar presentes ou favores de valor significativo de parceiros comerciais;</li>
                <li>Usar informações privilegiadas para benefício próprio;</li>
                <li>Manter atividades paralelas que concorram com a Arena BRB.</li>
            </ul>
            <p><strong>Qualquer situação de potencial conflito deve ser imediatamente comunicada ao superior hierárquico ou ao Comitê de Ética.</strong></p>
        </div>

        <div class="legal-section">
            <h2>6. Anticorrupção e Suborno</h2>
            <div class="info-card">
                <h3>Tolerância Zero à Corrupção</h3>
                <p>A Arena BRB adota postura de <strong>tolerância zero</strong> em relação a atos de corrupção, suborno, fraude ou qualquer prática ilícita.</p>
            </div>

            <h3>6.1. Proibições Absolutas</h3>
            <p>É estritamente proibido:</p>
            <ul>
                <li>Oferecer, prometer, dar ou solicitar vantagens indevidas;</li>
                <li>Realizar pagamentos ilegais ou "facilitação" a agentes públicos;</li>
                <li>Manipular processos licitatórios ou comerciais;</li>
                <li>Fraudar documentos ou informações;</li>
                <li>Participar de esquemas de propina ou lavagem de dinheiro;</li>
                <li>Utilizar intermediários para práticas ilícitas;</li>
                <li>Oferecer presentes ou hospitalidade excessiva que possam influenciar decisões.</li>
            </ul>

            <h3>6.2. Relacionamento com Setor Público</h3>
            <p>No relacionamento com agentes públicos e órgãos governamentais:</p>
            <ul>
                <li>Manter registros precisos e completos de todas as transações;</li>
                <li>Não oferecer ou pagar por favores, influência ou tratamento preferencial;</li>
                <li>Cumprir rigorosamente as leis de licitação e contratos públicos;</li>
                <li>Documentar adequadamente todas as interações relevantes;</li>
                <li>Reportar imediatamente qualquer solicitação imprópria.</li>
            </ul>

            <h3>6.3. Presentes e Hospitalidade</h3>
            <p>Presentes corporativos são permitidos apenas quando:</p>
            <ul>
                <li>De valor simbólico (até R$ 300,00);</li>
                <li>Ocasionais e não frequentes;</li>
                <li>Não visam influenciar decisões comerciais;</li>
                <li>São transparentes e registrados;</li>
                <li>Estão em conformidade com políticas do destinatário.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>7. Proteção de Informações e Dados</h2>

            <h3>7.1. Confidencialidade</h3>
            <p>Informações confidenciais da Arena BRB, clientes, parceiros e colaboradores devem ser protegidas, incluindo:</p>
            <ul>
                <li>Estratégias comerciais e planos de negócio;</li>
                <li>Dados financeiros e contábeis;</li>
                <li>Informações sobre fornecedores e contratos;</li>
                <li>Dados pessoais (conforme LGPD);</li>
                <li>Projetos em desenvolvimento;</li>
                <li>Propriedade intelectual.</li>
            </ul>

            <h3>7.2. Segurança da Informação</h3>
            <p>Todos devem:</p>
            <ul>
                <li>Utilizar senhas fortes e não compartilhá-las;</li>
                <li>Não acessar sistemas ou informações sem autorização;</li>
                <li>Proteger equipamentos e dispositivos de trabalho;</li>
                <li>Reportar incidentes de segurança imediatamente;</li>
                <li>Não utilizar redes Wi-Fi públicas para assuntos sensíveis;</li>
                <li>Fazer backup regular de informações importantes.</li>
            </ul>

            <h3>7.3. Uso de Recursos e Tecnologia</h3>
            <p>Equipamentos e sistemas da empresa devem ser usados para fins profissionais. É proibido:</p>
            <ul>
                <li>Acessar conteúdo ilegal, ofensivo ou inadequado;</li>
                <li>Fazer download de software não autorizado;</li>
                <li>Enviar spam ou correntes eletrônicas;</li>
                <li>Utilizar recursos da empresa para negócios pessoais;</li>
                <li>Divulgar informações da empresa em redes sociais sem autorização.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>8. Sustentabilidade e Responsabilidade Social</h2>

            <h3>8.1. Meio Ambiente</h3>
            <p>Comprometemo-nos com a proteção ambiental através de:</p>
            <ul>
                <li>Gestão eficiente de resíduos e reciclagem;</li>
                <li>Uso racional de recursos naturais (água, energia);</li>
                <li>Preferência por fornecedores ambientalmente responsáveis;</li>
                <li>Campanhas de conscientização ambiental;</li>
                <li>Cumprimento da legislação ambiental.</li>
            </ul>

            <h3>8.2. Responsabilidade Social</h3>
            <p>Atuamos como agentes de transformação social:</p>
            <ul>
                <li>Apoio a projetos culturais, esportivos e educacionais;</li>
                <li>Promoção da acessibilidade e inclusão;</li>
                <li>Parcerias com ONGs e instituições sociais;</li>
                <li>Incentivo ao voluntariado corporativo;</li>
                <li>Respeito aos direitos humanos.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>9. Comunicação e Relacionamento Externo</h2>

            <h3>9.1. Comunicação Institucional</h3>
            <p>Apenas porta-vozes autorizados podem falar em nome da Arena BRB com:</p>
            <ul>
                <li>Imprensa e mídia;</li>
                <li>Órgãos governamentais;</li>
                <li>Investidores e acionistas;</li>
                <li>Entidades de classe.</li>
            </ul>

            <h3>9.2. Redes Sociais</h3>
            <p>Ao se manifestar em redes sociais:</p>
            <ul>
                <li>Deixe claro que as opiniões são pessoais;</li>
                <li>Não divulgue informações confidenciais;</li>
                <li>Mantenha postura respeitosa e profissional;</li>
                <li>Não faça comentários depreciativos sobre a empresa, colegas ou concorrentes;</li>
                <li>Respeite direitos autorais e propriedade intelectual.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>10. Denúncias e Canal de Ética</h2>

            <h3>10.1. Dever de Reportar</h3>
            <p>Todos têm o dever de reportar violações ou suspeitas de violação deste Código, incluindo:</p>
            <ul>
                <li>Fraudes ou irregularidades financeiras;</li>
                <li>Corrupção ou suborno;</li>
                <li>Assédio ou discriminação;</li>
                <li>Conflito de interesses;</li>
                <li>Violações de segurança ou privacidade;</li>
                <li>Práticas ambientalmente prejudiciais;</li>
                <li>Qualquer conduta antiética.</li>
            </ul>

            <h3>10.2. Canal de Denúncias</h3>
            <div class="contact-info">
                <h4>Como Denunciar</h4>
                <p><strong>Canal de Ética - Denúncias Anônimas</strong></p>
