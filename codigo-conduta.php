                    Garantia de anonimato e confidencialidade
                </p>
            </div>

            <h3>10.3. Proteção ao Denunciante</h3>
            <p>A Arena BRB garante:</p>
            <ul>
                <li><strong>Confidencialidade:</strong> Identidade do denunciante será protegida;</li>
                <li><strong>Não Retaliação:</strong> Proibida qualquer forma de retaliação contra quem reportar de boa-fé;</li>
                <li><strong>Investigação Imparcial:</strong> Todas as denúncias serão investigadas adequadamente;</li>
                <li><strong>Medidas Corretivas:</strong> Ações apropriadas serão tomadas em caso de comprovação.</li>
            </ul>
            <p><strong>Atenção:</strong> Denúncias falsas ou de má-fé também são consideradas violações ao Código e serão tratadas adequadamente.</p>
        </div>

        <div class="legal-section">
            <h2>11. Compliance e Governança</h2>

            <h3>11.1. Programa de Compliance</h3>
            <p>A Arena BRB mantém programa estruturado de compliance que inclui:</p>
            <ul>
                <li>Due diligence de terceiros;</li>
                <li>Treinamentos periódicos obrigatórios;</li>
                <li>Auditorias internas e externas;</li>
                <li>Monitoramento contínuo de controles;</li>
                <li>Avaliação de riscos;</li>
                <li>Atualização de políticas e procedimentos.</li>
            </ul>

            <h3>11.2. Comitê de Ética</h3>
            <p>O Comitê de Ética é responsável por:</p>
            <ul>
                <li>Interpretar e esclarecer dúvidas sobre o Código;</li>
                <li>Receber, analisar e investigar denúncias;</li>
                <li>Propor melhorias e atualizações ao Código;</li>
                <li>Disseminar a cultura ética na organização;</li>
                <li>Aplicar medidas disciplinares quando necessário.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>12. Sanções Disciplinares</h2>
            <p>Violações a este Código podem resultar em medidas disciplinares proporcionais à gravidade:</p>
            <ul>
                <li><strong>Advertência verbal:</strong> Para infrações leves e primárias;</li>
                <li><strong>Advertência escrita:</strong> Para reincidências ou infrações de média gravidade;</li>
                <li><strong>Suspensão:</strong> Para violações graves;</li>
                <li><strong>Demissão por justa causa:</strong> Para infrações gravíssimas ou reincidentes;</li>
                <li><strong>Ações legais:</strong> Quando cabível, incluindo responsabilização civil e criminal.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>13. Compromisso com o Código</h2>
            <div class="highlight-box">
                <p>Todos os colaboradores e parceiros da Arena BRB devem conhecer, compreender e cumprir este Código de Conduta. A adesão a esses princípios é fundamental para manter nossa reputação, credibilidade e sustentabilidade no longo prazo.</p>
                <p><strong>O cumprimento deste Código é responsabilidade de cada um de nós. Juntos, construímos uma organização mais ética, íntegra e confiável.</strong></p>
            </div>
        </div>

        <div class="legal-section">
            <h2>14. Legislação Aplicável</h2>
            <p>Este Código está em conformidade com a legislação brasileira, incluindo:</p>
            <ul>
                <li>Lei Anticorrupção (Lei nº 12.846/2013)</li>
                <li>Lei Geral de Proteção de Dados - LGPD (Lei nº 13.709/2018)</li>
