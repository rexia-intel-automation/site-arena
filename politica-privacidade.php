        <h1 class="page-title">Política de Privacidade</h1>
        <p class="page-subtitle">Compromisso com a proteção dos seus dados pessoais conforme a LGPD</p>
    </section>

    <div class="legal-content">
        <div class="legal-section">
            <h2>1. Introdução</h2>
            <p>A Arena BRB Mané Garrincha, administrada pelo BRB - Banco de Brasília S.A., CNPJ nº 00.000.208/0001-00, está comprometida com a proteção da privacidade e dos dados pessoais de seus usuários, visitantes, clientes e parceiros.</p>
            <p>Esta Política de Privacidade foi desenvolvida em conformidade com a Lei Geral de Proteção de Dados Pessoais (Lei nº 13.709/2018 - LGPD) e descreve como coletamos, usamos, armazenamos, compartilhamos e protegemos seus dados pessoais ao utilizar nosso site, aplicativo Minha Arena BRB e demais serviços.</p>
            <div class="highlight-box">
                <p><strong>Importante:</strong> Ao utilizar nossos serviços, você concorda com as práticas descritas nesta Política de Privacidade. Recomendamos a leitura atenta deste documento.</p>
            </div>
        </div>

        <div class="legal-section">
            <h2>2. Definições</h2>
            <p>Para melhor compreensão desta política, considere as seguintes definições:</p>
            <ul>
                <li><strong>Dado Pessoal:</strong> Informação relacionada a pessoa natural identificada ou identificável;</li>
                <li><strong>Dado Pessoal Sensível:</strong> Dado pessoal sobre origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou organização religiosa, filosófica ou política, dado referente à saúde ou à vida sexual, dado genético ou biométrico;</li>
                <li><strong>Titular:</strong> Pessoa natural a quem se referem os dados pessoais;</li>
                <li><strong>Tratamento:</strong> Toda operação realizada com dados pessoais (coleta, armazenamento, uso, compartilhamento, eliminação, etc.);</li>
                <li><strong>Controlador:</strong> Arena BRB, responsável pelas decisões referentes ao tratamento de dados pessoais;</li>
                <li><strong>Encarregado (DPO):</strong> Pessoa indicada pelo controlador para atuar como canal de comunicação entre o controlador, os titulares e a ANPD;</li>
                <li><strong>ANPD:</strong> Autoridade Nacional de Proteção de Dados, órgão responsável por zelar pela proteção de dados pessoais no Brasil.</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>3. Dados Coletados</h2>
            <h3>3.1. Dados Fornecidos Voluntariamente</h3>
            <p>Coletamos os seguintes dados pessoais quando você os fornece voluntariamente:</p>
            <ul>
                <li><strong>Cadastro no site/aplicativo:</strong> Nome completo, CPF, e-mail, telefone, data de nascimento, endereço;</li>
                <li><strong>Compra de ingressos:</strong> Dados de pagamento (processados por parceiros certificados), histórico de compras;</li>
                <li><strong>Formulários de contato:</strong> Nome, e-mail, telefone, mensagem;</li>
                <li><strong>Newsletter:</strong> Nome, e-mail;</li>
                <li><strong>Currículos (RH):</strong> Dados profissionais, acadêmicos e experiências;</li>
                <li><strong>Eventos corporativos:</strong> Dados de empresas, representantes legais, CNPJ.</li>
            </ul>

            <h3>3.2. Dados Coletados Automaticamente</h3>
            <p>Quando você utiliza nosso site ou aplicativo, coletamos automaticamente:</p>
            <ul>
                <li>Endereço IP e localização geográfica aproximada;</li>
                <li>Tipo e versão do navegador/dispositivo;</li>
                <li>Sistema operacional;</li>
                <li>Páginas visitadas, data e hora de acesso;</li>
                <li>Origem da visita (referrer);</li>
                <li>Cookies e tecnologias similares (ver <a href="politica-cookies">Política de Cookies</a>).</li>
            </ul>

            <h3>3.3. Dados Biométricos (Reconhecimento Facial)</h3>
            <div class="info-card">
                <h4>Uso de Reconhecimento Biométrico no Aplicativo Minha Arena BRB</h4>
                <p><strong>Dados Sensíveis Coletados:</strong> Fotografia facial e dados biométricos derivados da análise da imagem do rosto (template biométrico).</p>

                <p><strong>Finalidades do Tratamento:</strong></p>
                <ul>
                    <li>Controle de acesso a eventos que exigem identificação biométrica por questões de segurança;</li>
                    <li>Prevenção de fraudes e revenda irregular de ingressos;</li>
                    <li>Agilização do processo de entrada em eventos;</li>
                    <li>Cumprimento de determinações legais e contratuais de segurança;</li>
                    <li>Proteção da integridade física dos participantes e do público.</li>
                </ul>

                <p><strong>Base Legal:</strong> Consentimento específico e destacado do titular (Art. 11, II, "a" da LGPD) e cumprimento de obrigação legal ou regulatória.</p>

                <p><strong>Eventos Aplicáveis:</strong> O reconhecimento biométrico será utilizado apenas em eventos que especificamente exigirem essa tecnologia, sendo o titular previamente informado no momento da compra do ingresso.</p>

                <p><strong>Consentimento e Revogação:</strong></p>
                <ul>
                    <li>O cadastramento da foto é <strong>voluntário e mediante consentimento expresso</strong>;</li>
                    <li>Você pode revogar seu consentimento a qualquer momento através do aplicativo Minha Arena BRB ou entrando em contato com o DPO;</li>
                    <li>A revogação pode impedir o acesso a eventos que exijam identificação biométrica;</li>
                    <li>Você tem o direito de solicitar a exclusão de seus dados biométricos.</li>
                </ul>

                <p><strong>Segurança dos Dados Biométricos:</strong></p>
                <ul>
                    <li>Os dados biométricos são criptografados e armazenados em servidores seguros com acesso restrito;</li>
                    <li>Utilizamos algoritmos de hash irreversíveis para proteção adicional;</li>
                    <li>As imagens faciais originais não são armazenadas após a geração do template biométrico;</li>
                    <li>O acesso aos dados biométricos é registrado e auditado regularmente;</li>
                    <li>Compartilhamento de dados biométricos com terceiros somente com seu consentimento explícito ou por determinação legal.</li>
                </ul>

                <p><strong>Retenção de Dados:</strong> Os dados biométricos serão mantidos apenas pelo período necessário para as finalidades descritas ou conforme exigido por lei, sendo eliminados após o término desse período.</p>
            </div>
        </div>

        <div class="legal-section">
            <h2>4. Finalidades do Tratamento de Dados</h2>
            <p>Utilizamos seus dados pessoais para as seguintes finalidades:</p>
            <h3>4.1. Execução de Contrato</h3>
            <ul>
                <li>Processar compras de ingressos e serviços;</li>
                <li>Gerenciar reservas e locações de espaços;</li>
                <li>Enviar confirmações, ingressos e informações sobre eventos;</li>
                <li>Prestar suporte ao cliente.</li>
            </ul>

            <h3>4.2. Cumprimento de Obrigações Legais</h3>
            <ul>
                <li>Atender requisições de autoridades competentes;</li>
                <li>Cumprir obrigações fiscais, contábeis e regulatórias;</li>
                <li>Garantir a segurança de eventos conforme legislação aplicável.</li>
            </ul>

            <h3>4.3. Interesses Legítimos</h3>
            <ul>
                <li>Melhorar nossos serviços e experiência do usuário;</li>
                <li>Realizar pesquisas de satisfação e análises estatísticas;</li>
                <li>Prevenir fraudes e garantir a segurança da plataforma;</li>
                <li>Proteger direitos e propriedade da Arena BRB.</li>
            </ul>

            <h3>4.4. Consentimento (Marketing)</h3>
            <ul>
                <li>Enviar comunicações de marketing sobre eventos, promoções e novidades;</li>
                <li>Personalizar ofertas e recomendações;</li>
                <li>Realizar campanhas publicitárias segmentadas.</li>
            </ul>
            <p>Você pode revogar seu consentimento para receber comunicações de marketing a qualquer momento.</p>
        </div>

        <div class="legal-section">
            <h2>5. Compartilhamento de Dados</h2>
            <p>A Arena BRB pode compartilhar seus dados pessoais com:</p>

            <h3>5.1. Parceiros e Prestadores de Serviços</h3>
            <ul>
                <li><strong>Plataformas de venda de ingressos:</strong> Para processar compras;</li>
                <li><strong>Processadores de pagamento:</strong> Para transações financeiras;</li>
                <li><strong>Provedores de tecnologia:</strong> Hospedagem, e-mail marketing, analytics;</li>
                <li><strong>Empresas de segurança:</strong> Para controle de acesso e monitoramento de eventos;</li>
                <li><strong>Organizadores de eventos:</strong> Quando necessário para realização do evento.</li>
            </ul>

            <h3>5.2. Autoridades Governamentais</h3>
            <p>Quando exigido por lei ou ordem judicial, podemos compartilhar dados com autoridades policiais, fiscais, regulatórias e judiciais.</p>

            <h3>5.3. Transferência Internacional de Dados</h3>
            <p>Alguns de nossos prestadores de serviços podem estar localizados fora do Brasil. Nesses casos, garantimos que sejam adotadas medidas adequadas de proteção conforme a LGPD.</p>

            <p><strong>Importante:</strong> A Arena BRB não vende, aluga ou comercializa dados pessoais de seus usuários.</p>
        </div>

        <div class="legal-section">
            <h2>6. Segurança dos Dados</h2>
            <p>Implementamos medidas técnicas e organizacionais para proteger seus dados pessoais contra acesso não autorizado, perda, destruição, alteração ou divulgação indevida:</p>
            <ul>
                <li>Criptografia de dados em trânsito (SSL/TLS) e em repouso;</li>
                <li>Controles de acesso baseados em funções e privilégios mínimos;</li>
                <li>Firewalls, sistemas de detecção de intrusão e monitoramento contínuo;</li>
                <li>Backups regulares e planos de recuperação de desastres;</li>
                <li>Treinamento de colaboradores sobre proteção de dados;</li>
                <li>Auditorias periódicas de segurança;</li>
                <li>Políticas internas de segurança da informação.</li>
            </ul>
            <p>Apesar de nossos esforços, nenhum sistema é 100% seguro. Em caso de incidente de segurança que possa gerar risco aos titulares, notificaremos conforme exigido pela LGPD.</p>
        </div>

        <div class="legal-section">
            <h2>7. Retenção de Dados</h2>
            <p>Mantemos seus dados pessoais apenas pelo tempo necessário para cumprir as finalidades para as quais foram coletados, observando:</p>
            <ul>
                <li><strong>Dados cadastrais:</strong> Durante a vigência do relacionamento e por até 5 anos após o encerramento, para cumprimento de obrigações legais;</li>
                <li><strong>Dados de transações:</strong> Pelo prazo legal de prescrição (10 anos) e obrigações fiscais;</li>
                <li><strong>Dados de marketing:</strong> Até a revogação do consentimento;</li>
                <li><strong>Dados biométricos:</strong> Conforme descrito na seção 3.3, com possibilidade de exclusão a pedido do titular;</li>
                <li><strong>Logs de acesso:</strong> Por até 6 meses, conforme Marco Civil da Internet.</li>
            </ul>
            <p>Após o período de retenção, os dados serão eliminados de forma segura ou anonimizados.</p>
        </div>

        <div class="legal-section">
            <h2>8. Direitos dos Titulares de Dados</h2>
            <p>Conforme a LGPD, você tem os seguintes direitos em relação aos seus dados pessoais:</p>
            <ul>
                <li><strong>Confirmação e acesso:</strong> Saber se tratamos seus dados e acessá-los;</li>
                <li><strong>Correção:</strong> Solicitar a correção de dados incompletos, inexatos ou desatualizados;</li>
                <li><strong>Anonimização, bloqueio ou eliminação:</strong> Solicitar a anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade;</li>
                <li><strong>Portabilidade:</strong> Solicitar a portabilidade dos dados a outro fornecedor de serviço;</li>
                <li><strong>Informação sobre compartilhamento:</strong> Saber com quem compartilhamos seus dados;</li>
                <li><strong>Informação sobre não consentimento:</strong> Ser informado sobre a possibilidade de não fornecer consentimento e as consequências;</li>
                <li><strong>Revogação do consentimento:</strong> Revogar seu consentimento a qualquer momento;</li>
                <li><strong>Oposição:</strong> Opor-se ao tratamento de dados realizado com base em interesses legítimos;</li>
                <li><strong>Revisão de decisões automatizadas:</strong> Solicitar revisão de decisões tomadas unicamente com base em tratamento automatizado.</li>
            </ul>

            <h3>Como exercer seus direitos:</h3>
            <div class="highlight-box">
                <p><strong>Aplicativo Minha Arena BRB:</strong> Acesse "Configurações" > "Privacidade e Dados"</p>
                <p><strong>E-mail do DPO:</strong> <a href="mailto:dpo@arenabrb.com.br">dpo@arenabrb.com.br</a></p>
                <p><strong>Formulário online:</strong> <a href="contato">www.arenabrb.com.br/contato</a></p>
            </div>
            <p>Responderemos sua solicitação em até 15 dias. Em caso de impossibilidade de atendimento imediato, informaremos os motivos e o prazo estimado.</p>
        </div>

        <div class="legal-section">
            <h2>9. Dados de Menores de Idade</h2>
            <p>Nossos serviços são destinados a maiores de 18 anos. Não coletamos intencionalmente dados de menores sem o consentimento dos pais ou responsáveis legais.</p>
            <p>Para eventos com público infantil, o cadastro e compra de ingressos devem ser realizados por responsáveis legais. O uso de reconhecimento biométrico para menores de idade requer consentimento específico de pelo menos um dos pais ou responsável legal.</p>
            <p>Se tomarmos conhecimento de que coletamos dados de menores sem consentimento adequado, tomaremos medidas para excluir tais informações o mais rápido possível.</p>
        </div>

        <div class="legal-section">
            <h2>10. Alterações nesta Política</h2>
            <p>Esta Política de Privacidade pode ser atualizada periodicamente para refletir mudanças em nossas práticas, legislação ou serviços. Notificaremos sobre alterações significativas por meio de:</p>
            <ul>
                <li>Aviso destacado em nosso site;</li>
                <li>E-mail para usuários cadastrados;</li>
                <li>Notificação no aplicativo Minha Arena BRB.</li>
            </ul>
            <p>Recomendamos que você revise esta política regularmente. O uso continuado de nossos serviços após as alterações constitui aceitação da nova política.</p>
        </div>

        <div class="legal-section">
            <h2>11. Encarregado de Proteção de Dados (DPO)</h2>
            <p>Nosso Encarregado de Proteção de Dados está disponível para esclarecer dúvidas, receber solicitações e atender reclamações relacionadas ao tratamento de dados pessoais:</p>
            <div class="contact-info">
                <h3>Entre em Contato com o DPO</h3>
                <p><strong>Nome:</strong> [Nome do DPO]</p>
                <p><strong>E-mail:</strong> <a href="mailto:dpo@arenabrb.com.br">dpo@arenabrb.com.br</a></p>
                <p><strong>Endereço:</strong> SRPN - Arena BRB Mané Garrincha, Brasília - DF, 70070-900</p>
            </div>
        </div>

        <div class="legal-section">
            <h2>12. Legislação e Foro</h2>
            <p>Esta Política de Privacidade é regida pela legislação brasileira, especialmente pela Lei nº 13.709/2018 (LGPD), Lei nº 12.965/2014 (Marco Civil da Internet) e Decreto nº 8.771/2016.</p>
            <p>Fica eleito o foro da comarca de Brasília, Distrito Federal, para dirimir quaisquer controvérsias decorrentes desta política.</p>
        </div>

        <div class="info-card">
            <h3>Seus dados estão protegidos</h3>
            <p>A Arena BRB está comprometida com a transparência e a proteção da sua privacidade. Tratamos seus dados com o mais alto padrão de segurança e em conformidade com a LGPD.</p>
            <p>Se você tiver dúvidas ou preocupações sobre como tratamos seus dados pessoais, entre em contato com nosso DPO. Você também tem o direito de apresentar reclamação à Autoridade Nacional de Proteção de Dados (ANPD).</p>
