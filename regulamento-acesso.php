        <p class="page-subtitle">Regras e normas para visitação ao Complexo Arena BRB</p>
    </section>

    <div class="legal-content">
        <div class="legal-section">
            <h2>1. Apresentação</h2>
            <p>Este Regulamento de Acesso estabelece as regras e condições para visitação e utilização das instalações do Complexo Arena BRB Mané Garrincha, localizado no SRPN - Setor de Recreação Pública Norte, Brasília - DF.</p>
            <p>O complexo é composto por: <strong>Estádio Mané Garrincha, Ginásio Nilson Nelson, Esfera (Espaço de Cultura e Eventos)</strong> e demais áreas do complexo esportivo.</p>
            <div class="highlight-box">
                <p><strong>Importante:</strong> Ao acessar qualquer área do complexo, você concorda em cumprir integralmente este regulamento. O descumprimento pode resultar em remoção das instalações e outras medidas cabíveis.</p>
            </div>
        </div>

        <div class="legal-section">
            <h2>2. Acesso em Dias SEM Eventos</h2>
            <p>Em dias sem eventos programados, o Complexo Arena BRB funciona como área de lazer e visitação pública.</p>

            <h3>2.1. Horário de Funcionamento</h3>
            <ul>
                <li><strong>Segunda a Sexta:</strong> 6h às 22h</li>
                <li><strong>Sábados, Domingos e Feriados:</strong> 6h às 22h</li>
            </ul>
            <p><em>Horários sujeitos a alterações. Consulte nosso site para informações atualizadas.</em></p>

            <h3>2.2. Áreas Abertas ao Público</h3>
            <ul>
                <li>Pista de caminhada e corrida ao redor do estádio</li>
                <li>Áreas verdes e jardins</li>
                <li>Estacionamentos públicos</li>
                <li>Áreas externas do complexo</li>
                <li>Espaços de convivência (quando disponíveis)</li>
            </ul>

            <h3>2.3. Áreas Restritas</h3>
            <ul>
                <li>Interior do estádio (arquibancadas, campo, vestiários)</li>
                <li>Interior do ginásio (exceto quando autorizado)</li>
                <li>Áreas administrativas e técnicas</li>
                <li>Salas de imprensa e transmissão</li>
                <li>Camarotes e áreas VIP</li>
                <li>Áreas de manutenção e operação</li>
            </ul>

            <h3>2.4. Atividades Permitidas</h3>
            <ul>
                <li>Caminhada, corrida e ciclismo nas áreas designadas</li>
                <li>Práticas esportivas leves (alongamento, yoga)</li>
                <li>Passeios em família</li>
                <li>Fotografia não comercial</li>
                <li>Piqueniques nas áreas gramadas permitidas</li>
            </ul>

            <h3>2.5. Atividades Proibidas</h3>
            <ul>
                <li>Acesso não autorizado a áreas restritas</li>
                <li>Comercialização de produtos sem autorização</li>
                <li>Consumo de bebidas alcoólicas</li>
                <li>Uso de substâncias ilícitas</li>
                <li>Sons amplificados ou que perturbem a ordem</li>
                <li>Pichação, depredação ou vandalismo</li>
                <li>Entrada de animais (exceto cães-guia)</li>
                <li>Soltar pipas ou objetos voadores</li>
                <li>Acender fogueiras ou churrasqueiras</li>
                <li>Jogar lixo fora das lixeiras</li>
            </ul>

            <h3>2.6. Estacionamento</h3>
            <ul>
                <li>Gratuito em dias sem eventos</li>
                <li>Sujeito à disponibilidade de vagas</li>
                <li>Limite de velocidade: 20 km/h</li>
                <li>Proibido estacionar em áreas não demarcadas</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>3. Acesso em Dias COM Eventos</h2>
            <p>Em dias de eventos (shows, jogos, feiras, congressos, etc.), aplicam-se regras específicas de acesso e segurança.</p>

            <h3>3.1. Ingresso Obrigatório</h3>
            <div class="info-card">
                <p><strong>É obrigatória a apresentação de ingresso válido</strong> para acesso às áreas do evento. O ingresso deve ser adquirido antecipadamente através das plataformas autorizadas.</p>
            </div>

            <h3>3.2. Documentação Necessária</h3>
            <ul>
                <li><strong>Ingresso válido (físico ou digital)</strong></li>
                <li><strong>Documento de identificação com foto (RG, CNH, Passaporte ou Carteira de Trabalho)</strong></li>
                <li>CPF (para eventos que exijam)</li>
                <li>Comprovante de meia-entrada (se aplicável): carteirinha de estudante, identidade jovem, doador de sangue, etc.</li>
                <li>Para menores de idade: autorização dos pais/responsáveis (conforme legislação)</li>
            </ul>

            <h3>3.3. Reconhecimento Biométrico Facial</h3>
            <div class="info-card">
                <h4>Eventos com Reconhecimento Biométrico</h4>
                <p>Alguns eventos podem exigir cadastro facial prévio através do aplicativo <strong>Minha Arena BRB</strong> para acesso mais ágil e seguro.</p>
                <p><strong>Como funciona:</strong></p>
                <ul>
                    <li>Cadastre sua foto no aplicativo antes do evento</li>
                    <li>No dia do evento, passe pelo portal de reconhecimento facial</li>
                    <li>O sistema validará automaticamente seu ingresso e identidade</li>
                    <li>Acesso liberado em segundos, sem filas</li>
                </ul>
                <p><strong>Importante:</strong> O cadastro biométrico é voluntário. Se preferir, você pode acessar através da validação tradicional de ingresso e documento.</p>
                <p>Consulte nossa <a href="politica-privacidade">Política de Privacidade</a> para informações sobre proteção de dados biométricos.</p>
            </div>

            <h3>3.4. Horários de Abertura dos Portões</h3>
            <ul>
                <li>Os portões abrem geralmente <strong>2 horas antes</strong> do início do evento</li>
                <li>Recomenda-se chegar com antecedência para evitar filas</li>
                <li>Portões fecham no início do evento ou conforme determinação da organização</li>
            </ul>

            <h3>3.5. Revista e Segurança</h3>
            <p>Para garantir a segurança de todos, será realizada revista pessoal e de pertences:</p>
            <ul>
                <li>Revista por detector de metais</li>
                <li>Inspeção visual de bolsas, mochilas e objetos</li>
                <li>Revista manual quando necessário (por segurança do mesmo gênero)</li>
                <li>Possibilidade de revista mais rigorosa em eventos de alto risco</li>
            </ul>
            <p><strong>A recusa em passar pela revista implica na proibição de acesso, sem direito a reembolso.</strong></p>

            <h3>3.6. Itens Proibidos em Eventos</h3>
            <ul>
                <li>Armas de qualquer tipo (de fogo, brancas, de eletrochoque, sprays de pimenta)</li>
                <li>Fogos de artifício, sinalizadores, bombinhas</li>
                <li>Objetos pontiagudos, cortantes ou contundentes</li>
                <li>Substâncias inflamáveis ou explosivas</li>
                <li>Bebidas alcoólicas e drogas ilícitas</li>
                <li>Recipientes de vidro, latas, garrafas térmicas</li>
                <li>Guarda-chuvas com ponta metálica</li>
                <li>Bastões de selfie, tripés profissionais</li>
                <li>Instrumentos musicais (exceto quando autorizado)</li>
                <li>Drones ou equipamentos de voo</li>
                <li>Correntes, cinturões com fivelas grandes</li>
                <li>Bandeiras com mastro rígido ou pontiagudo</li>
                <li>Materiais de propaganda política ou ofensivos</li>
                <li>Animais (exceto cães-guia devidamente identificados)</li>
                <li>Alimentos e bebidas em grande quantidade</li>
                <li>Equipamentos profissionais de filmagem (exceto credenciados)</li>
                <li>Laser pointers</li>
            </ul>
            <p><em>A organização do evento pode estabelecer restrições adicionais específicas.</em></p>

            <h3>3.7. Itens Permitidos</h3>
            <ul>
                <li>Bolsas pequenas e mochilas (sujeitas a revista)</li>
                <li>Smartphones e câmeras compactas para uso pessoal</li>
                <li>Medicamentos de uso pessoal (com receita médica se controlados)</li>
                <li>Alimentos para necessidades especiais (bebês, diabéticos)</li>
                <li>Protetor solar, boné, óculos de sol</li>
                <li>Cadeiras de rodas e andadores</li>
                <li>Aparelhos auditivos e próteses</li>
            </ul>

            <h3>3.8. Setores e Assentos</h3>
            <ul>
                <li>Cada pessoa deve ocupar apenas o assento indicado em seu ingresso</li>
                <li>É proibida a troca de setores sem autorização</li>
                <li>Respeite os assentos destinados a PCD e idosos</li>
                <li>Não obstrua corredores, saídas de emergência ou áreas de circulação</li>
            </ul>

            <h3>3.9. Conduta Durante o Evento</h3>
            <ul>
                <li>Respeite os demais espectadores e a equipe de organização</li>
                <li>Não fume em áreas proibidas (ambientes fechados e arquibancadas)</li>
                <li>Mantenha o celular no silencioso durante apresentações</li>
                <li>Descarte o lixo nas lixeiras apropriadas</li>
                <li>Siga as instruções da equipe de segurança e brigadistas</li>
                <li>Em caso de emergência, mantenha a calma e siga as orientações</li>
            </ul>

            <h3>3.10. Proibições de Conduta</h3>
            <ul>
                <li>Manifestações racistas, homofóbicas, sexistas ou discriminatórias</li>
                <li>Arremesso de objetos</li>
                <li>Invasão de campo, palco ou áreas restritas</li>
                <li>Brigas, agressões físicas ou verbais</li>
                <li>Nudez ou comportamento obsceno</li>
                <li>Uso de linguagem ofensiva</li>
                <li>Filmagem ou fotografia para fins comerciais sem autorização</li>
                <li>Venda ambulante não autorizada</li>
                <li>Porte ou uso de substâncias ilícitas</li>
                <li>Consumo excessivo de álcool ou comportamento de embriaguez</li>
            </ul>

            <h3>3.11. Estacionamento em Dias de Evento</h3>
            <ul>
                <li>Cobrado conforme tabela do evento</li>
                <li>Vagas limitadas - sujeito à lotação</li>
                <li>Vagas preferenciais para idosos e PCD</li>
                <li>Proibido estacionar em locais não autorizados (remoção por guincho)</li>
                <li>Recomenda-se o uso de transporte público ou aplicativos de transporte</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>4. Normas Específicas por Local</h2>

            <h3>4.1. Estádio Mané Garrincha</h3>
            <ul>
                <li>Capacidade: 72.000 pessoas</li>
                <li>Eventos: Jogos de futebol, shows, grandes eventos</li>
                <li>Setores: Arquibancadas, cadeiras, camarotes, áreas VIP</li>
                <li>Acessibilidade: Elevadores e rampas para PCD</li>
            </ul>

            <h3>4.2. Ginásio Nilson Nelson</h3>
            <ul>
                <li>Capacidade: 11.000 pessoas</li>
                <li>Eventos: Shows, competições esportivas, eventos corporativos</li>
                <li>Setores: Arquibancadas, piso, camarotes</li>
                <li>Acessibilidade: Elevadores e áreas específicas para PCD</li>
            </ul>

            <h3>4.3. Esfera (Espaço de Cultura e Eventos)</h3>
            <ul>
                <li>Capacidade variável conforme configuração</li>
                <li>Eventos: Exposições, feiras, congressos, eventos culturais</li>
                <li>Espaços modulares e adaptáveis</li>
                <li>Totalmente acessível</li>
            </ul>

            <h3>4.4. Outras Áreas do Complexo</h3>
            <ul>
                <li>Pista de atletismo</li>
                <li>Quadras poliesportivas</li>
                <li>Áreas de convivência</li>
                <li>Praças e jardins</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>5. Acessibilidade e Atendimento Especial</h2>

            <h3>5.1. Pessoas com Deficiência (PCD)</h3>
            <ul>
                <li>Assentos e áreas específicas em todos os setores</li>
                <li>Acompanhante de PCD tem direito a assento adjacente</li>
                <li>Elevadores e rampas de acesso</li>
                <li>Banheiros adaptados</li>
                <li>Vagas de estacionamento preferenciais</li>
                <li>Cães-guia são permitidos em qualquer evento</li>
            </ul>

            <h3>5.2. Idosos (60+)</h3>
            <ul>
                <li>Filas preferenciais</li>
                <li>Assentos preferenciais quando disponíveis</li>
                <li>Vagas de estacionamento próximas aos acessos</li>
            </ul>

            <h3>5.3. Gestantes e Lactantes</h3>
            <ul>
                <li>Atendimento preferencial</li>
                <li>Fraldários disponíveis</li>
                <li>Permissão para entrada com alimentos para bebês</li>
            </ul>

            <h3>5.4. Crianças e Adolescentes</h3>
            <ul>
                <li>Menores de 5 anos não pagam ingresso (no colo)</li>
                <li>Classificação etária conforme o evento</li>
                <li>Menores desacompanhados precisam de autorização judicial</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>6. Direitos de Imagem</h2>
            <p>Ao acessar o complexo, especialmente durante eventos, você está ciente e concorda que:</p>
            <ul>
                <li>Pode ser filmado ou fotografado por câmeras de segurança e de transmissão</li>
                <li>Sua imagem pode ser utilizada em transmissões televisivas, internet e materiais promocionais</li>
                <li>Não há direito a compensação financeira pelo uso de imagem em contexto de evento público</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>7. Medidas Disciplinares</h2>
            <p>O descumprimento deste regulamento pode resultar em:</p>
            <ul>
                <li><strong>Advertência verbal</strong> pela equipe de segurança</li>
                <li><strong>Remoção do local</strong> sem direito a reembolso de ingresso</li>
                <li><strong>Proibição de entrada</strong> em eventos futuros por período determinado</li>
                <li><strong>Registro de ocorrência policial</strong> em casos graves</li>
                <li><strong>Responsabilização civil e criminal</strong> por danos causados</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>8. Emergências e Evacuação</h2>
            <p>Em caso de emergência:</p>
            <ul>
                <li>Mantenha a calma e não corra</li>
                <li>Siga as instruções da equipe de segurança e brigadistas</li>
                <li>Dirija-se às saídas de emergência sinalizadas</li>
                <li>Não utilize elevadores em caso de incêndio</li>
                <li>Auxilie pessoas com dificuldade de locomoção</li>
                <li>Aguarde autorização para retornar às instalações</li>
            </ul>
            <p><strong>Em caso de emergência médica, procure imediatamente um membro da equipe ou dirija-se ao posto médico.</strong></p>
        </div>

        <div class="legal-section">
            <h2>9. Cancelamento ou Adiamento de Eventos</h2>
            <ul>
                <li>A Arena BRB reserva-se o direito de cancelar ou adiar eventos por motivos de força maior, segurança ou determinação de autoridades</li>
                <li>Políticas de reembolso seguem os termos específicos de cada evento</li>
                <li>Acompanhe comunicados oficiais pelo site e redes sociais</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>10. Responsabilidades</h2>

            <h3>10.1. Da Arena BRB</h3>
            <ul>
                <li>Manter as instalações em condições adequadas de uso</li>
                <li>Garantir a segurança dos visitantes dentro das instalações</li>
                <li>Prestar serviços de primeiros socorros e emergência médica</li>
                <li>Cumprir legislação de acessibilidade e proteção ao consumidor</li>
            </ul>

            <h3>10.2. Do Visitante/Espectador</h3>
            <ul>
                <li>Cumprir integralmente este regulamento</li>
                <li>Zelar pela conservação das instalações</li>
                <li>Respeitar demais visitantes e funcionários</li>
                <li>Responsabilizar-se por danos causados ao patrimônio</li>
                <li>Guardar seus pertences (a Arena não se responsabiliza por perdas ou furtos)</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>11. Objetos Perdidos</h2>
            <ul>
                <li>Objetos encontrados devem ser entregues à administração</li>
                <li>Objetos perdidos ficam guardados por até 90 dias</li>
                <li>Para resgatar, apresente documento de identificação e descreva o objeto</li>
                <li>Contato: <a href="mailto:achados@arenabrb.com.br">achados@arenabrb.com.br</a> ou (61) 3333-3333</li>
            </ul>
        </div>

        <div class="legal-section">
            <h2>12. Alterações do Regulamento</h2>
            <p>A Arena BRB reserva-se o direito de alterar este regulamento a qualquer momento. Alterações entram em vigor imediatamente após publicação no site oficial.</p>
            <p>Eventos específicos podem ter regulamentos complementares que devem ser observados em conjunto com este regulamento geral.</p>
        </div>

        <div class="legal-section">
            <h2>13. Legislação Aplicável</h2>
            <p>Este regulamento está em conformidade com:</p>
            <ul>
                <li>Estatuto do Torcedor (Lei nº 10.671/2003)</li>
                <li>Código de Defesa do Consumidor (Lei nº 8.078/1990)</li>
