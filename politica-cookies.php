            <p>Alguns cookies podem coletar dados pessoais, como:</p>
            <ul>
                <li>Endereço IP</li>
                <li>Identificadores únicos de dispositivo</li>
                <li>Dados de navegação e comportamento</li>
                <li>Informações de geolocalização</li>
            </ul>
            <p>O tratamento desses dados está descrito em nossa <a href="politica-privacidade">Política de Privacidade</a> e está em conformidade com a LGPD.</p>
        </div>

        <div class="legal-section">
            <h2>9. Retenção de Dados de Cookies</h2>
            <p>Os dados coletados através de cookies são retidos conforme os períodos especificados para cada tipo de cookie. Cookies de sessão são excluídos quando você fecha o navegador, enquanto cookies persistentes permanecem pelo período definido (geralmente de alguns dias a 2 anos).</p>
        </div>

        <div class="legal-section">
            <h2>10. Segurança</h2>
            <p>Implementamos medidas de segurança para proteger os dados coletados através de cookies:</p>
            <ul>
                <li>Cookies sensíveis são criptografados;</li>
                <li>Uso de flags HttpOnly e Secure para cookies de autenticação;</li>
                <li>Implementação de políticas SameSite para proteção contra CSRF;</li>
                <li>Monitoramento regular de cookies de terceiros;</li>
                <li>Auditorias periódicas de segurança.</li>
            </ul>
        </div>

