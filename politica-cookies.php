        <div class="legal-section">
            <h2>5. Outras Tecnologias de Rastreamento</h2>
            <p>Além de cookies, utilizamos outras tecnologias similares:</p>

            <h3>5.1. Web Beacons (Pixel Tags)</h3>
            <p>Pequenas imagens gráficas incorporadas em páginas web ou e-mails para rastrear visualizações e interações.</p>

            <h3>5.2. Local Storage e Session Storage</h3>
            <p>Tecnologias HTML5 que permitem armazenar dados localmente no navegador para melhorar a performance e experiência do usuário.</p>

            <h3>5.3. Fingerprinting</h3>
            <p>Técnica que coleta informações sobre configurações do dispositivo e navegador para criar um identificador único. Utilizamos apenas para fins de segurança e prevenção de fraudes.</p>
        </div>

