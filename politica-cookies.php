                <button onclick="alert('Painel de gerenciamento de cookies será implementado')" style="background: var(--color-primary); color: white; border: none; padding: 15px 30px; border-radius: 8px; font-size: 1rem; cursor: pointer; font-weight: 600;">
                    Gerenciar Preferências de Cookies
                </button>
            </div>

            <h3>6.2. Configurações do Navegador</h3>
            <p>Você também pode controlar cookies através das configurações do seu navegador:</p>
            <ul>
                <li><strong>Google Chrome:</strong> Configurações > Privacidade e segurança > Cookies e outros dados de sites</li>
                <li><strong>Mozilla Firefox:</strong> Opções > Privacidade e Segurança > Cookies e dados de sites</li>
                <li><strong>Safari:</strong> Preferências > Privacidade > Cookies e dados de sites</li>
                <li><strong>Microsoft Edge:</strong> Configurações > Privacidade, pesquisa e serviços > Cookies</li>
            </ul>
            <p><strong>Atenção:</strong> Bloquear ou excluir cookies pode afetar a funcionalidade do site e sua experiência de navegação.</p>

