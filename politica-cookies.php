        <div class="legal-section">
            <h2>1. O que são Cookies?</h2>
            <p>Cookies são pequenos arquivos de texto armazenados no seu dispositivo (computador, tablet ou smartphone) quando você visita um site. Eles são amplamente utilizados para fazer os sites funcionarem de forma mais eficiente, bem como para fornecer informações aos proprietários do site.</p>
            <p>Os cookies permitem que o site reconheça seu dispositivo e armazene algumas informações sobre suas preferências ou ações anteriores, melhorando sua experiência de navegação.</p>
        </div>

        <div class="legal-section">
            <h2>2. Por que Utilizamos Cookies?</h2>
            <p>A Arena BRB utiliza cookies e tecnologias similares para:</p>
            <ul>
                <li><strong>Essenciais:</strong> Garantir o funcionamento básico do site e permitir a navegação;</li>
                <li><strong>Desempenho:</strong> Analisar como os visitantes utilizam o site e melhorar sua performance;</li>
                <li><strong>Funcionalidade:</strong> Lembrar suas preferências e personalizar sua experiência;</li>
                <li><strong>Marketing:</strong> Exibir anúncios relevantes e medir a eficácia de campanhas publicitárias.</li>
            </ul>
            <div class="highlight-box">
                <p><strong>Conformidade com a LGPD:</strong> O uso de cookies que coletam dados pessoais está em conformidade com a Lei Geral de Proteção de Dados (Lei nº 13.709/2018).</p>
            </div>
        </div>

        <div class="legal-section">
            <h2>3. Tipos de Cookies que Utilizamos</h2>

            <h3>3.1. Cookies Essenciais (Necessários)</h3>
            <p>Estes cookies são estritamente necessários para o funcionamento do site. Eles permitem que você navegue pelo site e use suas funcionalidades.</p>
            <div class="info-card">
                <h4>Exemplos:</h4>
                <ul>
                    <li><strong>session_id:</strong> Mantém sua sessão ativa durante a navegação</li>
                    <li><strong>cookie_consent:</strong> Armazena suas preferências de cookies</li>
                    <li><strong>security_token:</strong> Protege contra ataques CSRF</li>
                </ul>
                <p><strong>Duração:</strong> Sessão ou até 1 ano</p>
                <p><strong>Base Legal:</strong> Interesse legítimo (necessário para o funcionamento do site)</p>
            </div>

            <h3>3.2. Cookies de Desempenho e Análise</h3>
            <p>Estes cookies coletam informações sobre como os visitantes usam nosso site, como as páginas mais visitadas e mensagens de erro. Todos os dados coletados são agregados e anônimos.</p>
            <div class="info-card">
                <h4>Exemplos:</h4>
                <ul>
                    <li><strong>Google Analytics (_ga, _gid, _gat):</strong> Analisam o comportamento dos usuários</li>
                    <li><strong>Hotjar:</strong> Mapas de calor e gravações de sessão</li>
                </ul>
                <p><strong>Duração:</strong> De 1 dia a 2 anos</p>
                <p><strong>Base Legal:</strong> Consentimento do usuário</p>
            </div>

            <h3>3.3. Cookies de Funcionalidade</h3>
            <p>Estes cookies permitem que o site se lembre de escolhas que você faz (como seu nome de usuário, idioma ou região) e forneçam recursos aprimorados e mais personalizados.</p>
            <div class="info-card">
                <h4>Exemplos:</h4>
                <ul>
                    <li><strong>theme_preference:</strong> Lembra se você prefere tema claro ou escuro</li>
                    <li><strong>language:</strong> Armazena sua preferência de idioma</li>
                    <li><strong>location_preference:</strong> Lembra sua localização preferencial</li>
                </ul>
                <p><strong>Duração:</strong> Até 1 ano</p>
                <p><strong>Base Legal:</strong> Consentimento do usuário</p>
            </div>

            <h3>3.4. Cookies de Marketing e Publicidade</h3>
            <p>Estes cookies são usados para rastrear visitantes em diferentes sites e exibir anúncios relevantes e atraentes para usuários individuais.</p>
            <div class="info-card">
                <h4>Exemplos:</h4>
                <ul>
                    <li><strong>Google Ads (_gcl_au):</strong> Rastreia conversões de anúncios</li>
                    <li><strong>Facebook Pixel (fr, _fbp):</strong> Rastreia interações para publicidade direcionada</li>
                    <li><strong>LinkedIn Insights:</strong> Análise de campanhas no LinkedIn</li>
                </ul>
                <p><strong>Duração:</strong> De 3 meses a 2 anos</p>
                <p><strong>Base Legal:</strong> Consentimento do usuário</p>
            </div>

            <h3>3.5. Cookies de Terceiros</h3>
            <p>Alguns cookies são colocados por serviços de terceiros que aparecem em nosso site. A Arena BRB não controla esses cookies, e você deve verificar os sites de terceiros para mais informações.</p>
            <ul>
                <li>Google Analytics</li>
                <li>Google Ads</li>
                <li>Facebook/Meta</li>
                <li>YouTube (vídeos incorporados)</li>
                <li>Plataformas de venda de ingressos</li>
            </ul>
        </div>

